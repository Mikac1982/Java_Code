package com.class2;

public class AritmeticOperators {

	public static void main(String[] args) {

		int a = 30;
		int b = 15;

		System.out.println(a + b); // 45
		System.out.println(a - b); // 15
		System.out.println(a * b); // 450
		System.out.println(a / b); // 2

		int sum = a + b;
		int sub = a - b;
		int mult = a * b;
		int div = a / b;

		System.out.println(sum); // 45
		System.out.println(sub); // 15
		System.out.println(mult); // 450
		System.out.println(div); // 2

		// the __ of 2 numbers is __

		System.out.println("the addition of 2 numbers is " + sum);
		System.out.println("the substraction of 2 numbers is " + sub);
		System.out.println("the multiplitation of 2 numbers is " + mult);
		System.out.println("the division of 2 numbers is " + div);
	}
}
