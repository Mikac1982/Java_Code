package com.class10;

public class Allvalues {
   public static void main(String[] args) {
	   
	                //  0,  1,  2,  3,  4,  5     //index=size-1 (5=6-1)
	   char[] grades= {'A','B','C','D','E','F'};  // size is 6!
	   
	   
	   for (int i=0; i<=grades.length-1; i++) {  //same as i<grades.lenght; (i<=grades.lenght;)
		   System.out.println(grades[i]);
		   
	   }
	   
	   
	   
}
}
