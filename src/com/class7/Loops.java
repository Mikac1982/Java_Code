package com.class7;

public class Loops {
  public static void main(String[] args) {
	
	  // to eliminate repetition we use LOOPS!
	  
	System.out.println("Good morning");
	System.out.println("Good morning");
	System.out.println("Good morning");
	System.out.println("Good morning");
	 
	// Loops in Java:
	// 1. while
	// 2. do while
	// 3. for
	// enhanced foor loop (for each loop)// collections or arrays
//----------------------------------------------------------------------------	 
	// WHILE LOOP 
	
	boolean morning=true;
    if (morning) {
    	System.out.println("Good morning");  // //only when it's true; print only one time!
    }
	while (morning) {
		System.out.println("Good morning");  //infinite times
	}
    int a=1;
    
    while (a<10) {
   	System.out.println("");
    }
  }  
  }

